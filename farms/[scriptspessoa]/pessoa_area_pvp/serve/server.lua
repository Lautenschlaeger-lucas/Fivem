local Tunnel = module("vrp","lib/Tunnel")
local Proxy = module("vrp","lib/Proxy")
local Tools = module("vrp","lib/Tools")
vRP = Proxy.getInterface("vRP")
vRPclient = Tunnel.getInterface("vRP")

src = {}
Tunnel.bindInterface("pessoa_area_pvp",src)
------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
------------------------------------------------------------------------- FUNCTION TELEPORT ----------------------------------------------------------------------------------
------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
function src.teleport()
	local source = source
	local user_id = vRP.getUserId(source)
	local cdsrand = math.random(100)
	local player = vRP.getUserSource(parseInt(user_id))

	if user_id then
		
		if parseInt(cdsrand) > 99 then
			vRPclient.teleport(player,-2177.9, 5186.37, 16.99)
		end
		
		if parseInt(cdsrand) > 70 and parseInt(cdsrand) < 98 then
			vRPclient.teleport(player,-2190.45, 5200.46, 18.93)
		end

		if parseInt(cdsrand) > 40 and parseInt(cdsrand) < 69 then
			vRPclient.teleport(player,-2168.02, 5226.75, 17.3) 
		end

		if parseInt(cdsrand) > 1 and parseInt(cdsrand) < 39 then
			vRPclient.teleport(player,-2194.58, 5167.15, 13.52) 
		end
	end
end
------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
------------------------------------------------------------------------- FUNCTION TELEPORT BACK -----------------------------------------------------------------------------
function src.teleportBack()
	local source = source
	local user_id = vRP.getUserId(source)
	local data = vRP.getUserDataTable(user_id)
	if user_id then
		vRPclient.teleport(source,51.92, -876.53, 30.4)
	end
end
------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
---------------------------------------------------------------------------- FUNCTION CHECK ----------------------------------------------------------------------------------
------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
function src.checkPermissao()
	local source = source
	local user_id = vRP.getUserId(source)
	if vRP.hasPermission(user_id,"evento.permissao") then
		return true
	end
	return false
end
------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
---------------------------------------------------------------------------- FUNCTION RESPAWN --------------------------------------------------------------------------------
------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
function src.Respawn()
	local source = source
	local user_id = vRP.getUserId(source)
	if user_id then
		vRPclient.killGod(source)
		vRPclient.setArmour(source,200)
		vRPclient.setHealth(source,400)
		vRP.varyThirst(source,-100)
		vRP.varyHunger(source,-100)
	end
end
------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
------------------------------------------------------------------------------ FUNCTION ADD ----------------------------------------------------------------------------------
------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
function src.addGroup()
	local source = source
	local user_id = vRP.getUserId(source)
	if user_id then
		vRP.addUserGroup(user_id,"evento")
		return true
	end
	return false
end
------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
------------------------------------------------------------------------------ FUNCTION REM ----------------------------------------------------------------------------------
------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
function src.remGroup()
	local source = source
	local user_id = vRP.getUserId(source)
	if user_id then
		vRP.removeUserGroup(user_id,"evento")
		return true
	end
	return false
end
